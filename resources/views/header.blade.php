<script>
    // Wait for the DOM to be fully loaded
    document.addEventListener('DOMContentLoaded', function() {
        // Get the button element
        var dropdownButton = document.getElementById('userDropdownButton');

        // Get the dropdown menu element
        var dropdownMenu = document.getElementById('logoutDropdown');

        // Add event listener to the button for click event
        dropdownButton.addEventListener('click', function(event) {
            // Prevent the default action of the button
            event.preventDefault();

            // Toggle the 'hidden' class on the dropdown menu
            dropdownMenu.classList.toggle('hidden');
        });

        // Add event listener to the document to close dropdown when clicking outside
        document.addEventListener('click', function(event) {
            // Check if the clicked element is not the dropdown button or the dropdown menu itself
            if (!dropdownButton.contains(event.target) && !dropdownMenu.contains(event.target)) {
                // Hide the dropdown menu
                dropdownMenu.classList.add('hidden');
            }
        });
    });
</script>